// Single Responsibility Principle (SRP)
// EN: A class should have only one reason to change — it should do only one job.
// AZ: Bir class yalnız bir is gorsun, lazimsiz ve cox is gormesi bu prinsipi pozur




// Open Closed Principle (OCP)
// EN: Classes should be open for extension but closed for modification (add new behavior without changing existing code).
// AZ: Kod genislenmeye aciq olmali amma mentiqi deyisikliklere qapali olmalidir.




// Liskov Substitution Principle (LSP)
// EN: Objects of a subclass should be replaceable with objects of the base class without breaking the program.
// AZ: Subclass obyektleri proqrami pozmadan base class obyektlerinin yerine istifade edile bilmelidir. 




// Interface Segregation Principle (ISP)
// EN: Clients should not be forced to depend on methods/interfaces they don't use — prefer many small interfaces over one big one.
// AZ:Her bir seyi eyni Interface qoymaq yerine ayri-ayri Interface yaratmaq daha mentiqlidir.




// Dependency Inversion Principle (DIP)
// EN: High-level modules should not depend on low-level modules — both should depend on abstractions (interfaces).
// AZ: Qisasi Class-lar birbasa bir-birine baglanmasin, arada interface olsun — biri dəyisende o birine tesir etmesin.